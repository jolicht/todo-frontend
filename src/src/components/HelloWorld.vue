<template>
    <div class="hello">
        <h1>{{ msg }}</h1>
        <p>
            Database says it's: {{ timeInfo.time }}
        </p>
    </div>
</template>

<script>
    import axios from 'axios';

    export default {
        name: 'HelloWorld',
        props: {
            msg: String
        },
        data() {
            return {
                timeInfo: null
            }
        },
        mounted() {
            axios.get('http://localhost:9080/api/time/current')
                .then(response => (this.timeInfo = response.data));
        }
    }
</script>

